import Foundation
import SwiftUI

struct SettingsViewForm<Content: View>: View {
    let content: Content

    init(@ViewBuilder content: () -> Content) {
        self.content = content()
    }

    var body: some View {
        VStack(alignment: .leading, spacing: 8) {
            content
        }
        .padding(.vertical, 8)
        .padding(.horizontal)
        .cornerRadius(8)
        .scrollIfLargerThanContainer()
    }
}
